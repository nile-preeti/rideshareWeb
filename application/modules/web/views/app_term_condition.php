        <p>It's important for TNC drivers to be aware of their insurance coverage. When the TNC app is off, the driver's personal auto insurance applies. Once the app is on, there are different periods with varying coverage. During Period 1 and 2, TNCs often offer contingent liability coverage. Period 3, when a passenger is in the vehicle, is covered by the TNC's primary policy.</p>
