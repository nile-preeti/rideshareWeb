        <h1>Transportation Network Company (TNC) Insurance Policy</h1>
        <h2>Coverage Requirements:</h2>
        <h3>Rideshare Auto Insurance:</h3>
        <p>- All TNC drivers must maintain rideshare auto insurance coverage at all times while the TNC app is turned on.</p>
        <p>- This coverage specifically addresses liabilities arising during rideshare activities and is distinct from personal liability, comprehensive, and collision auto insurance policies.</p>

         <h3>Liability Coverage:</h3>
         <p>- Minimum liability coverage must meet or exceed the local regulatory requirements for rideshare drivers.</p>
         <p>- This coverage applies to bodily injury and property damage caused to third parties during TNC-related activities.</p>
         <h3>Uninsured/Underinsured Motorist Coverage:</h3>
        <p> - TNC drivers are required to carry uninsured/underinsured motorist coverage to protect against accidents involving at-fault drivers without adequate insurance.</p>
        <h3>Medical Payments Coverage:</h3>
        <p>- Medical payments coverage is strongly recommended to cover medical expenses for TNC drivers and passengers in the event of an accident, regardless of fault.</p>
        
        <h3>*Comprehensive and Collision Coverage (Optional):</h3>
        <p> - While not mandatory, TNC drivers are encouraged to consider comprehensive and collision coverage to protect against damages to their own vehicle, subject to their individual preferences and risk tolerance.</p>
        <h3>Periods of Coverage:</h3>
        <p>- Coverage is mandatory from the moment the TNC driver activates the app to accept rides until the completion of the ride or the driver goes offline.</p>
        <h3>Insurance Carrier Approval:</h3>
        <p>- TNC drivers must obtain approval from their insurance carrier to ensure their policy covers rideshare activities.</p>
        <h3>Policy Verification:</h3>
        <p> - TNC drivers must regularly provide proof of rideshare insurance coverage to the TNC platform, ensuring compliance with the established insurance requirements.</p>
        <h3>Notification of Changes:</h3>
        <p>- TNC drivers must promptly notify their insurance carrier of any changes in their driving habits or status that may affect their rideshare insurance coverage.</p>
        <h3>Non-Compliance Consequences:</h3>
        <p>- Failure to maintain the required rideshare insurance coverage may result in suspension or termination of the TNC driver account.</p>

        <p><b>By adhering to these insurance policy guidelines, TNC drivers contribute to a safer and more responsible rideshare environment. Please consult with your insurance provider for specific details and ensure continuous compliance with local regulations.</b></p>
